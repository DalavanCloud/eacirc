#define STAT_DATA_CORRUPTED                     3       // GIVEN DATA ARE NOT IN EXPECTED FORMAT
#define STAT_DATA_INCORRECT_LENGTH              4       // LENGTH OF GIVEN DATA IS INCORRECT
#define STAT_FILE_OPEN_FAIL                     5       // FAIL TO OPEN TARGET FILE
#define STAT_NOT_ENOUGHT_DATA_TYPE              6       // GIVEN DATA VARIABLE IS UNABLE TO CONTAIN RETURN VALUE
#define STAT_USERDATA_BAD                       7       // DATA OBTAINED FROM USER INPUT ARE INVALID
