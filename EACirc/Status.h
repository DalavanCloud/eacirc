#define STAT_CONFIG_INCORRECT                   11      // CONFIGURATION IS NOT CORRECT
#define STAT_FILE_WRITE_FAIL                    12      // FILE COULD NOT BE SAVED
#define STAT_CONFIG_SCRIPT_INCOMPLETE           13      // CONFIGURATION FILE IS INCOMPLETE
#define STAT_PROJECT_ERROR                      14      // ERROR IN PROJECT CLASSES
#define STAT_INTENTIONAL_EXIT                   15      // EXITING EACIRC INTENTIONALLY
#define STAT_FILE_READ_FAIL                     16      // READ FROM FILE UNSUCCESSFUL
#define STAT_EXTERNAL_SYSTEM_FAILED             17      // SOME EXTERNAL SYSTEM (E.G. MAPPING NET DRIVE) FAILED
#define STAT_CIRCUIT_INCONSISTENT               18      // CIRCUIT INCONSISTENT - UNKNOWN FUNCTIONS, UNSUPPORTED CONNECTOR MASK, ETC.
#define STAT_CIRCUIT_BACKEND_ERROR              19      // PROBLEM WITH CIRCUIT BACKEND

